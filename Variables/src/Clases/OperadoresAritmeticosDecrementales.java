package Clases;

public class OperadoresAritmeticosDecrementales {
	
	public static void main(String args[]){
		
		// -- a la derecha (a--) Decrementa el valor de la variable después de usarla en una expresión.
		// -- a la izquierda (--a) Decrementa el valor de la variable antes de usarla en una expresión.
		int a = 5;
		int b = a--;
		System.out.println(a);
		System.out.println(b);
	}	
}

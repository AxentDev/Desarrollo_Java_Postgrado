package Clases;

public class OperadoresAritmeticosIncrementales {
	

	public static void main(String args[]){
		
		// ++ a la derecha (a++) Incrementa el valor de la variable después de que se ha utilizado en una expresión.
		// ++ a la izquierda (++a) Incrementa el valor de la variable antes de que se utilice en una expresión.
		int a = 5;
		int b = a++;
		System.out.println(a);
		System.out.println(b);
	}
}

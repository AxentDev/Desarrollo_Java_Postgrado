package ClasesOperadoresReview2;

public class Ejercicio2 {
	
	public static void main(String args[]) {
		
		int variableUno = 9;
		int variableDos = 7;
		
		double sumaVariables = variableUno + variableDos;
		System.out.println("El resultado de la suma es: " + sumaVariables);
		double restaVariables = variableUno - variableDos;
		System.out.println("El resultado de la resta es: " + restaVariables);
		double multiplicacionVariables = variableUno * variableDos;
		System.out.println("El resultado de la multiplicacion es: " + multiplicacionVariables);
		double divisionVariables = variableUno / variableDos;
		System.out.println("El resultado de la division es: " + divisionVariables);
		
		
	}
	

}

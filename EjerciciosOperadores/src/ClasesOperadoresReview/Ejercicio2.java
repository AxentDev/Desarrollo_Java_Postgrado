package ClasesOperadoresReview;

public class Ejercicio2 {
	
	public static void main(String args[]) {
		
		int numero1 = 9;
		int numero2 = 7;
		
		
		double sumaNumeros = numero1+numero2;
		System.out.println("El resultado de la suma es: " +sumaNumeros);
		double restaNumeros = numero1-numero2;
		System.out.println("El resultado de la resta es: " +restaNumeros);
		double multiplicacionNumero = numero1*numero2;
		System.out.println("El resultado de la multiplicacion es: " +multiplicacionNumero);
		double divisionNumeros = numero1/numero2;
		System.out.println("El resultado de la division es: " +divisionNumeros);
		
		
		
		
	}

}

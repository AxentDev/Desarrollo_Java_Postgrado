package ClasesOperadoresReview;

public class Ejercicio3 {
	
	public static void main(String args[]) {
		
		int numeroUno = 8;
		int numeroDos = 2;
		
		System.out.println("El valor numero uno es: " +numeroUno);
		System.out.println("El valor numero dos es: " +numeroDos);
		
		int temp = numeroUno;
		
		numeroUno = numeroDos;
		numeroDos = temp;
		
		System.out.println("El intercambio de valores para el numero uno es: " +numeroUno);
		System.out.println("El intercambio de valores para el numero dos es: " +numeroDos);
		
		
		
		
		
	}

}

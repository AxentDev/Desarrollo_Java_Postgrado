package GuiaSegundoModulo;

public class SwitchcaseConString {
	public static void main(String args[])
	{
		String nombre = "Ana";
		switch (nombre)
		{
		case "Perro":
			System.out.println("Hola Perro, lindo día");
			break;
		case "Juan":
			System.out.println("Hola Juan, como estás");
			break;
		case "Stiven":
			System.out.println("Hola Stiven, buena barba");
			break;
		case "Ana":
			System.out.println("Hola Ana, lindo pelo");
			break;
		case "Susana":
			System.out.println("Hola Susana, bonito auto");
			break;
		default:
			System.out.println("Error");
		}
			
	}

}

package GuiaSegundoModulo;

public class Matrices2 
{
	public static void main(String args[])
	{
		int edades[][] = {{19,18,16},{15,14,12},{12,11,18}};

		for (int i = 0; i < 3; i++)
		{
			for (int j = 0; j < 3; j++)
			{
				System.out.print(edades[i][j] + " ");
			}
			System.out.println("");
		}
		
	}

}

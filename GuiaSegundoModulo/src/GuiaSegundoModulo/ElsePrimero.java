public class ElsePrimero {
	public static void main(String args[]) 
	{	
		String nombre = "Alejo";//Se puede cambiar el nombre para comprobar si es true o false.
		if (nombre.equals("Alejo")) {
			System.out.println("True");
		}
		else
		{
			System.out.println("False");
		}
	}

}

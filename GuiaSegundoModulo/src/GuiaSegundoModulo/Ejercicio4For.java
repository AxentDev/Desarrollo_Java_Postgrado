package GuiaSegundoModulo;

public class Ejercicio4For 
{
	public static void main(String args[])
	{
		//Desarrollar un problema que imprima las tablas de multiplicar del 1 al 10 con su respectivo resultado.
		
		//Primero utilizo el ciclo for.
		for (int i = 1; i<=10; i++)
		{
			System.out.println("Tabla de multiplicar "+i);
			for (int j = 1; j<=10; j++)
			{
				System.out.println(i+" * "+j+ " = " +(i*j));
			}
			
		}
	}
}
